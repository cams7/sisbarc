package br.com.cams7.sisbarc.aal.vo;

public class Led {
	private String cor;
	private Boolean acesa;

	public Led() {
		super();
	}

	public String getCor() {
		return cor;
	}

	public void setCor(String cor) {
		this.cor = cor;
	}

	public Boolean getAcesa() {
		return acesa;
	}

	public void setAcesa(Boolean acesa) {
		this.acesa = acesa;
	}

}